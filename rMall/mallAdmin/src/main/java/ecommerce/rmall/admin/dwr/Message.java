package ecommerce.rmall.admin.dwr;

import java.util.Date;

public class Message {  
    private int id;  
    private String msg;  
    private Date time;  
    public int getId() {  
        return id;  
    }  
    public void setId(int id) {  
        this.id = id;  
    }  
    public String getMsg() {  
        return msg;  
    }  
    public void setMsg(String msg) {  
        this.msg = msg;  
    }  
    public Date getTime() {  
        return time;  
    }  
    public void setTime(Date time) {  
        this.time = time;  
    }  
}  